namespace Elixir::Application
{
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

    bool Is_Focused()
    {
        DWORD zApplication;
        ::GetWindowThreadProcessId(::GetForegroundWindow(), & zApplication);

        if (zApplication == ::GetCurrentProcessId())
        {
            wchar_t zName[MAX_PATH];
            ::GetWindowTextW(::GetForegroundWindow(), zName, MAX_PATH);

            return Expr_Hash(zName) != Expr_Hash(L"Explorer");
        }
        return false;
    }

    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-
    // -=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

    bool Is_Pressed(uint32_t Key)
    {
        return GetAsyncKeyState(Key) & (1 << 16);
    }
}

